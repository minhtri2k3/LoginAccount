    private DefaultListModel <String> Set_up_Vitamin(String fruitname){
           DefaultListModel <String> DML = new DefaultListModel<>();
      
         PreparedStatement pst = null;
 try{
            String temp = "Select product_vitamins from fruit where product_name = ?; ";
            pst = connection.prepareCall(temp);
            pst.setString(1, fruitname);
            ResultSet rs = pst.executeQuery(); 
            while(rs.next()){
                String item = rs.getString("product_vitamins");
                DML.addElement(item);
            }
          
        }catch(Exception e){
           e.printStackTrace();
        }

      return DML;
    }
